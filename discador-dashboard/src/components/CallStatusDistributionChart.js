import React from 'react';
import { Pie } from 'react-chartjs-2';
import {
  Chart as ChartJS,
  ArcElement,
  Tooltip,
  Legend
} from 'chart.js';

ChartJS.register(
  ArcElement,
  Tooltip,
  Legend
);

const CallStatusDistributionChart = ({ callStats }) => {
  const chartData = {
    labels: ['Respondidas', 'Não Respondidas', 'Abandonadas', 'Falhas'],
    datasets: [
      {
        data: [callStats.answered, callStats.not_answered, callStats.abandoned, callStats.failed],
        backgroundColor: [
          'rgba(75, 192, 192, 0.2)',
          'rgba(255, 99, 132, 0.2)',
          'rgba(255, 206, 86, 0.2)',
          'rgba(153, 102, 255, 0.2)'
        ],
        borderColor: [
          'rgba(75, 192, 192, 1)',
          'rgba(255, 99, 132, 1)',
          'rgba(255, 206, 86, 1)',
          'rgba(153, 102, 255, 1)'
        ],
        borderWidth: 1,
      },
    ],
  };

  const options = {
    responsive: true,
    plugins: {
      legend: {
        position: 'top',
      },
      title: {
        display: true,
        text: 'Distribuição de Status das Chamadas',
      },
    },
  };

  return <Pie data={chartData} options={options} />;
};

export default CallStatusDistributionChart;
